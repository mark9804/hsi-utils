# Pre-build pchip interpolators (computed once at import time)
_LAMBDA = _CIE_1964_RAW[:, 0]
_INTERP_X = PchipInterpolator(_LAMBDA, _CIE_1964_RAW[:, 1], extrapolate=False)
_INTERP_Y = PchipInterpolator(_LAMBDA, _CIE_1964_RAW[:, 2], extrapolate=False)
_INTERP_Z = PchipInterpolator(_LAMBDA, _CIE_1964_RAW[:, 3], extrapolate=False)

# XYZ to sRGB matrix (D65 illuminant)
# Standard IEC 61966-2-1 matrix
_XYZ_TO_SRGB = np.array(
    [
        [3.2404542, -1.5371385, -0.4985314],
        [-0.9692660, 1.8760108, 0.0415560],
        [0.0556434, -0.2040259, 1.0572252],
    ]
)


def wavelength_to_xyz(wavelength_nm: float | np.ndarray) -> np.ndarray:
    """Convert wavelength(s) to CIE XYZ tristimulus values.

    Args:
        wavelength_nm: scalar or array of wavelengths in nm.

    Returns:
        (..., 3) array of XYZ values.
    """
    wl = np.asarray(wavelength_nm, dtype=np.float64)
    x = np.nan_to_num(_INTERP_X(wl), nan=0.0)
    y = np.nan_to_num(_INTERP_Y(wl), nan=0.0)
    z = np.nan_to_num(_INTERP_Z(wl), nan=0.0)
    return np.stack([x, y, z], axis=-1)


def _srgb_gamma(linear: np.ndarray) -> np.ndarray:
    """Apply sRGB gamma correction (linear -> sRGB)."""
    out = np.where(
        linear <= 0.0031308,
        12.92 * linear,
        1.055 * np.power(np.clip(linear, 0, None), 1.0 / 2.4) - 0.055,
    )
    return np.clip(out, 0.0, 1.0)


def wavelength_to_srgb(wavelength_nm: float | np.ndarray) -> np.ndarray:
    """Convert wavelength(s) to sRGB color in [0, 1].

    Uses CIE 1964 10-degree color matching functions, XYZ->sRGB transform,
    and standard sRGB gamma correction.

    Args:
        wavelength_nm: scalar or array of wavelengths in nm.

    Returns:
        (..., 3) array of sRGB values in [0, 1].
    """
    xyz = wavelength_to_xyz(wavelength_nm)
    linear_rgb = xyz @ _XYZ_TO_SRGB.T
    linear_rgb = np.clip(linear_rgb, 0.0, None)
    # Normalize so max channel = 1 (match MATLAB spectrumRGB behavior)
    max_val = linear_rgb.max(axis=-1, keepdims=True)
    max_val = np.where(max_val > 0, max_val, 1.0)
    linear_rgb = linear_rgb / max_val
    return _srgb_gamma(linear_rgb)
